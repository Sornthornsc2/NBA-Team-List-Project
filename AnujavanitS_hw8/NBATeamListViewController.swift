    override func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
        let team = teams[indexPath.row]
        let cell = tableView.dequeueReusableCell(withIdentifier: team.located.rawValue, for: indexPath)

        // Configure the cell...
        cell.textLabel?.text = team.name
        cell.detailTextLabel?.text = team.shortDescription
        return cell
    }


    override func tableView(_ tableView: UITableView, didSelectRowAt indexPath: IndexPath){
        let tittle = "NBA Team List"
        let message = "You have selected \(teams[indexPath.row].name)"
        
        let alertController = UIAlertController(title: tittle, message: message, preferredStyle: .alert)
        let okAction = UIAlertAction(title: "OK", style: .default, handler: nil)
        alertController.addAction(okAction)
        present(alertController, animated: true, completion: nil)
        self.tableView.deselectRow(at: indexPath, animated: true)
    }
    
    override func tableView(_ tableView: UITableView, accessoryButtonTappedForRowWith indexPath: IndexPath){
        let team = teams[indexPath.row]
        let tittle = team.name
        let message = team.longDescription
